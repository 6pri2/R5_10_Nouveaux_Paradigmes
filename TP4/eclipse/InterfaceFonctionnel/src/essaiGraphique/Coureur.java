package essaiGraphique;

public class Coureur {
	
	protected String num;
	protected String nom;
	protected String prenom;
	protected String anneeNaissance;
	protected String anneePrem;
	
	public Coureur(String num, String nom, String prenom, String anneeNaissance, String anneePrem) {
		this.num = num;
		this.nom = nom;
		this.prenom = prenom;
		this.anneeNaissance = anneeNaissance;
		this.anneePrem = anneePrem;
	}
	

}
